package ru.geekbrains.lesson1.regular;

/**
 * Класс в котором есть методы сумирования, умножения и деления чисел
 */
public class OtherClass {
    /**
     * Функция сложения двух целых цисел
     *
     * @param a первое слогаемое
     * @param b второе слогаемое
     * @return возврат результата сложения a и b
     */
    public static int sum(int a, int b) {
        return a + b;
    }

    /**
     * Функция умножения двух целых цисел
     *
     * @param a первый множитель
     * @param b второй множитель
     * @return возврат результата умножения a и b
     */
    public static int mul(int a, int b) {
        return a * b;
    }


    /**
     * Функция деления двух целых цисел
     *
     * @param a первый делитель
     * @param b второй делитель
     * @return возврат результата деления a и b
     */
    public static int div(int a, int b) { 
        return a / b;
    }

}
